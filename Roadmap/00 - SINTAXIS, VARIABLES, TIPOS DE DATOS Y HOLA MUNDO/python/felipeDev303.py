# Las variables en Python se definen con el nombre de la variable seguido de un signo igual y el valor que se le asigna.
my_variable = "Esta es mi variable"

# La misma variable puede ser reasignada con otro valor.
my_variable = "Este es mi nuevo valor"

# Las constantes en Python por convención se definen con mayusculas y guiones bajos.
MY_CONSTANT = "Esta es mi constante"

# Tipos de datos en Python

# Enteros (int)
my_int = 10

# Flotantes (float)
my_float = 0.5

# Cadenas de texto (str)
my_string = "Hola Mundo"

# Booleanos (bool)
my_bool = True
my_bool = False

# Para imprimir en consola se utiliza la función print()
print("Hola Mundo")

# Para determinar el tipo de dato
print(type(my_int))
print(type(my_float))
print(type(my_string))
print(type(my_bool))