# Variables
nombre = 'Ximena Cortes' # Variable tipo String
lenguaje = 'Python' # Variable tipo String
year = 2024 # Variable tipo int
estatura = 69.5 # Variable tipo float
interes_en_python = True # Varuiable de tipo booleano 
NUM_PI=3.1416 #Variable tipo Constante

Saludo = '¡Hola, Python!'
Saludo2 = 'Hola', lenguaje
print(Saludo)
print(Saludo2, '\n')

print("=== Imprimir tipo de variables y constante ===")
print ('==============================================')
print(type(nombre), '->', nombre)
print(type(lenguaje),'->', lenguaje)
print(type(year), '->',year)
print(type(estatura), '->', estatura)
print(type(interes_en_python), '->', interes_en_python)
print(type(NUM_PI), 'Constante ->',NUM_PI)
