/*Utilizando tu lenguaje, emplea 3 mecanismos diferentes para imprimir
números del 1 al 10 mediante iteración.*/

//For
for (let i = 1; i <= 10; i++) {
    console.log(i)
}

//While
let i = 1;
while (i <= 10) {
    console.log(i)
    i++
}

//Do...while
let i = 1;
do {
    console.log(i)
    i++
} while (i <= 10);
