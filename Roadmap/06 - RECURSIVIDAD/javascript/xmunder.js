const printNumbers100to0 = (num) => {
  if (num < 0) return;
  console.log(num);
  printNumbers100to0(num - 1);
};

printNumbers100to0(10);

// Dificultad extra

const factorial = (n) => {
  if (n === 0 || n === 1) {
    return 1;
  } else {
    return n * factorial(n - 1);
  }
};

console.log(factorial(5));

function fibonacci(n) {
  if (n === 0) {
    return 0;
  } else if (n === 1) {
    return 1;
  } else {
    return fibonacci(n - 1) + fibonacci(n - 2);
  }
}

console.log(fibonacci(10));
