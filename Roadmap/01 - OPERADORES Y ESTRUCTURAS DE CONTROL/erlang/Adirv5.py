"""
Operadores
"""
# Operadores Aritméticos
print(f"Suma: 10 + 3 = {10 + 3}")
print(f"Resta: 10 - 3 = {10 - 3}")
print(f"Multiplicación: 10 * 3 = {10 * 3}")
print(f"División: 10 / 3 = {10 / 3}")
print(f"Módulo: 10 % 3 = {10 % 3}")
print(f"Exponenciación: 10 ** 3 = {10 ** 3}")
print(f"División Entera: 10 // 3 = {10 // 3}")

# Operadores de Comparación
print(f"igual a: 10 == 3 = {10 == 3}")
print(f"diferente de: 10 != 3 = {10 != 3}")
print(f"mayor que: 10 > 3 = {10 > 3}")
print(f"menor que: 10 < 3 = {10 < 3}")
print(f"mayor o igual que: 10 >= 3 = {10 >= 3}")
print(f"menor o igual que: 10 <= 3 = {10 <= 3}")

# Operadores Lógicos
print(f"AND &&: 10 + 3 == 13 and 5 - 1 == 4 es {10 + 3 == 13 and 5 - 1 == 4}")
print(f"OR ||: 10 + 3 == 13 or 5 - 1 == 4 es {10 + 3 == 14 or 5 - 1 == 4}")
print(f"NOT !: not 10 + 3 == 14 es {not 10 + 3 == 14}")

# Operadores de Asignación
my_number = 11 # asignacion 
print(my_number)
my_number += 2 # suma y asignacion
print(my_number)
my_number -= 3 # resta y asignacion 
print(my_number)
my_number *= 2 # multiplicacion y asignacion
print(my_number)
my_number /= 2 # division y asignacion
print(my_number)
my_number %= 3 # modulo y asignacion
print(my_number)
my_number //= 1 # division entera y asignacion
print(my_number)   
my_number **= 2 # exponenciacion y asignacion
print(my_number)

# Operadores de Identidad
my_new_number = my_number
print(f"my_number es my_new_number: {my_number is my_new_number}")
print(f"my_number no es my_new_number: {my_number is not my_new_number}")

# Operadores de Pertenencia
print(f"'u' in 'Python es un lenguaje de programación': {'u' in 'Python es un lenguaje de programación'}")
print(f"'b' not in 'Python es un lenguaje de programación': {'b' not in 'Python es un lenguaje de programación'}")

# Operadores de bit
print(f"AND 10 & 3 = {10 & 3}") # 0010 en binario
print(f"OR 10 | 3 = {10 | 3}") # 1011 en binario
print(f"XOR 10 ^ 3 = {10 ^ 3}") # 1001 en binario
print(f"NOT ~10 = {~10}") # 0101 en binario
print(f"Desplazamiento a la derecha: 10 >> 1 = {10 >> 1}") # 0010
print(f"Desplazamiento a la izquierda: 10 << 1 = {10 << 1}") # 101000

