#01 OPERADORES Y ESTRUCTURAS DE CONTROL

#ARITMETICOS
#suma
suma = 1 + 2
print("suma: ", suma)

#ARITMETICOS
#resta
resta = 3 - 2
print("resta: ", resta)

#multiplicacion
multiplicacion = 3 * 2
print("multiplicacion: ", multiplicacion)

#resta
division = 4 / 2
print("division: ", division)

#Logicos
#and
and_logico = True and True
print("and: ", and_logico)
#or
or_logico = True or False
print("or: ", or_logico)
#not
not_logico = not True
print("not: ", not_logico)

#Comparacion

#igual
igual = 4 == 4
print("igual: ", igual)
#diferent
diferente = 1 != 2
print("diferente: ", diferente)
#mayor que
mayor_que = 2 > 1
print("mayor que: ", mayor_que)
#menor que
menor_que = 1 < 2
print("menor que: ", menor_que)
#mayor o igual que
mayor_o_igual_que = 2 >= 1
print("mayor o igual que: ", mayor_o_igual_que)
#menor o igual que
menor_o_igual_que = 1 <= 2
print("menor o igual que: ", menor_o_igual_que)

#Identidad
#is
isIdentidad = 1 is 1
print("is: ", isIdentidad)
#is not
isNotIdentidad = 1 is not 2
print("is not: ", isNotIdentidad)

#in
inPertenencia = 1 in [1,2,3]    
print("in: ", inPertenencia)
#not in
notInPertenencia = 1 not in [1,2,3]
print("not in: ", notInPertenencia)



